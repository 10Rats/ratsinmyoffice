function loadtimeline() {
    const tlid = document.getElementsByClassName("timeline")[0].id;
    document.getElementsByClassName("timeline")[0].innerHTML = 
        `<div class="container" style="margin-left: 400px;" id="topcontainer">
        </div>
        <hr>
        <div class="container" id="bottomcontainer">
        </div>`;
    if (tlid == "local"){
        for (let i = 0; i < local_timeline.length; i++) {
            const data_year = local_timeline[i].year;
            const data_description = local_timeline[i].description;
            const data_image = local_timeline[i].image;
            addhtml(i, data_year, data_description, data_image);
        };
    }
    else if (tlid == "omega") { 
        for (let i = 0; i < omega_timeline.length; i++) {
            const data_year = omega_timeline[i].year;
            const data_description = omega_timeline[i].description;
            const data_image = omega_timeline[i].image;
            addhtml(i, data_year, data_description, data_image);
        };
    };
}

