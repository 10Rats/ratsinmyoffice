function toggleid() {
    const checkBox = document.getElementById("toggleeverything");
    const background = document.getElementsByTagName("body")[0];
    const imgs = document.getElementsByTagName("img");
    const lines = document.getElementsByClassName("line");
    const links = document.getElementsByTagName("a");
    if (checkBox.checked == true){
        document.getElementsByClassName("timeline")[0].id = "omega";
        loadtimeline();
        background.style.backgroundColor = "black";
        background.style.color = "white";
        for (let i = 0; i < lines.length; i++) {
            lines[i].style.backgroundColor = "white";
        }
        for (let i = 0; i < imgs.length; i++) {
            imgs[i].style.borderColor = "white";
        }
        for (let i = 0; i < links.length; i++) {
            links[i].style.color = "red";
        }
    } 
    else {
        document.getElementsByClassName("timeline")[0].id = "local";
        loadtimeline();
        background.style.backgroundColor = "white";
        background.style.color = "black";
        for (let i = 0; i < lines.length; i++) {
            lines[i].style.backgroundColor = "black";
        }
        for (let i = 0; i < imgs.length; i++) {
            imgs[i].style.borderColor = "black";
        }
        for (let i = 0; i < links.length; i++) {
            links[i].style.color = "rgb(0, 72, 255)";
        }
    }
}

function addhtml(i, data_year, data_description, data_image) {
    if (i % 2 !== 0) { //true if odd
        document.getElementById("topcontainer").innerHTML +=
            `<div class="up" id="tl${i}">
                <div class="line"></div>
                <div class="body">
                    <h1>${data_year}</h1>
                    <div class="splittwo">
                        <center>${data_description}</center>
                    </div>
                </div>
            </div>`
    }
    else { //true if even
        document.getElementById("bottomcontainer").innerHTML +=
            `<div class="down" id="tl${i}">
                <div class="line"></div>
                <div class="body">
                    <h1>${data_year}</h1>
                    <div class="splittwo">
                        <center>${data_description}</center>
                    </div>
                </div>
            </div>`
    }
}
